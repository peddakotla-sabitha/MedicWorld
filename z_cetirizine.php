				<h2>What is cetirizine?</h2><br><br>
					Cetirizine is a second generation antihistamine that reduces the natural chemical histamine in the body. 						Histamine can produce symptoms of sneezing, itching, watery eyes, and runny nose.<br><br>

					Cetirizine is used to treat cold or allergy symptoms such as sneezing, itching, watery eyes, or runny 						nose.<br><br>

					Cetirizine is also used to treat an allergic reaction, itching and swelling caused by chronic urticaria 					(hives) and minimizes or eliminates the symptoms of perennial allergic rhinitis, seasonal allergic rhinitis, 						chronic idiopathic urticaria, allergic asthma, physical urticaria, and atopic dermatitis.<br><br>
	
					<h3>Warnings</h3><br><br>
					Cetirizine can cause side effects that may impair your thinking or reactions. Be careful if you drive or do 						anything that requires you to be awake and alert. Avoid drinking alcohol. It can increase some of the side 						effects of cetirizine.<br><br>

					Tell your doctor if you regularly use other medicines that make you sleepy (such as other cold or allergy 						medicine, narcotic pain medicine, sleeping pills, muscle relaxers, and medicine for seizures, depression, or 						anxiety). They can add to sleepiness caused by cetirizine.<br><br>

					Call your doctor if your symptoms do not improve, if they get worse, or if you also have a fever.<br><br>

					<h3>Before taking this medicine</h3><br><br>
					You should not use this medication if you are allergic to or had an adverse reaction to cetirizine.<br><br>

					Before taking cetirizine, tell your doctor about all of your medical conditions or if you have ever had kidney 						or liver disease.<br><br>

					Cetirizine is not expected to be harmful to an unborn baby. Tell your doctor if you are pregnant or plan to 						become pregnant during treatment. Cetirizine can pass into breast milk and may harm a nursing baby. Do not use 						cetirizine without telling your doctor if you are breast-feeding a baby.<br><br>

					Cetirizine is indicated for chronic idiopathic urticaria in adults and children 6 months of age and 						older.<br><br>			
	
					Older adults may need to take a lower than normal dose. Follow your doctor's instructions.<br><br>

					<h3>How should I take cetirizine?</h3><br><br>
					Take cetirizine exactly as directed on the label, or as prescribed by your doctor. Do not use it in larger 						amounts or for longer than recommended.<br><br>

					You may take cetirizine with or without food.<br><br>

					The chewable tablet must be chewed before you swallow it.<br><br>

					Oral doses: measure liquid medicine (oral solution) with a special dose-measuring spoon or cup, not a regular 						table spoon. If you do not have a dose-measuring device, ask your pharmacist for one.<br><br>

					Call your doctor if your symptoms do not improve, if they get worse, or if you also have a fever. Store 					cetirizine at room temperature away from moisture and heat.<br><br>


	
