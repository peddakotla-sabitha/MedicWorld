				<h2>What is aspirin?</h2><br><br>
				Aspirin is a salicylate (sa-LIS-il-ate). It works by reducing substances in the body that cause pain, 					fever, and inflammation.<br><br>

						Aspirin is used to treat pain, and reduce fever or inflammation. It is sometimes used to treat or 							prevent heart attacks, strokes, and chest pain (angina).<br><br>

						Aspirin should be used for cardiovascular conditions only under the supervision of a doctor.<br><br>

						<h2>Warnings</h2><br><br>
						You should not use aspirin if you have a bleeding disorder such as hemophilia, a recent history of 							stomach or intestinal bleeding, or if you are allergic to an NSAID (non-steroidal anti-inflammatory 							drug) such as Advil, Motrin, Aleve, Orudis, Indocin, Lodine, Voltaren, Toradol, Mobic, Relafen, 						Feldene, and others.<br><br>

						Do not give this medication to a child or teenager with a fever, flu symptoms, or chickenpox. 							Salicylates can cause Reye's syndrome, a serious and sometimes fatal condition in children.<br><br>

						<h2>Before taking this medicine</h2><br><br>
						Do not give this medicine to a child or teenager with a fever, flu symptoms, or chickenpox. Aspirin 							can cause Reye's syndrome, a serious and sometimes fatal condition in children.<br><br>

						You should not use aspirin if you are allergic to it, or if you have:<br><br>

						a recent history of stomach or intestinal bleeding;<br><br>

						a bleeding disorder such as hemophilia; or<br><br>

						if you have ever had an asthma attack or severe allergic reaction after taking aspirin or an NSAID 							(non-steroidal anti-inflammatory drug) such as Advil, Motrin, Aleve, Orudis, Indocin, Lodine, 							Voltaren, Toradol, Mobic, Relafen, Feldene, and others.<br><br>

						To make sure this medicine is safe for you, tell your doctor if you have:<br><br>

						asthma or seasonal allergies;<br><br>

						stomach ulcers;<br><br>

						liver disease;<br><br>

						kidney disease;<br><br>

						a bleeding or blood clotting disorder;<br><br>

						gout; or<br><br>

						heart disease, high blood pressure, or congestive heart failure.<br><br>

						Taking aspirin during late pregnancy may cause bleeding in the mother or the baby during delivery. 							Tell your doctor if you are pregnant or plan to become pregnant.<br><br>

						Aspirin can pass into breast milk and may harm a nursing baby. You should not breastfeed while using 							this medicine.<br><br>

						<h2>How should I take aspirin?</h2><br><br>
						Take aspirin exactly as directed on the label, or as prescribed by your doctor. Do not use in larger 							or smaller amounts or for longer than recommended.<br><br>

						Always follow directions on the medicine label about giving aspirin to a child.<br><br>

						Take with food if aspirin upsets your stomach.<br><br>

						You must chew the chewable tablet before you swallow it.<br><br>

						Do not crush, chew, break, or open an enteric-coated or delayed/extended-release pill. Swallow the 							pill whole.<br><br>

						If you need surgery, tell the surgeon ahead of time that you are using this medicine. You may need to 							stop using it for a short time.<br><br>

						Do not use aspirin if you smell a strong vinegar odor in the bottle. The medicine may no longer be 							effective.<br><br>

						Store at room temperature away from moisture and heat.<br><br>
						 




	
