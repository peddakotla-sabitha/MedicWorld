			<h2>What is diclofenac?</h2><br><br>
			Diclofenac is a nonsteroidal anti-inflammatory drug (NSAID). This medicine works by reducing substances in the body that cause 				pain and inflammation.<br><br>

			Diclofenac is used to treat mild to moderate pain, or signs and symptoms of osteoarthritis or rheumatoid arthritis. Voltaren 				is also indicated for the treatment of ankylosing spondylitis. The Cataflam brand of this medicine is also used to treat 				menstrual cramps.<br><br>

			Diclofenac powder (Cambia) is used to treat a migraine headache attack. Cambia will only treat a headache that has already 				begun. It will not prevent headaches or reduce the number of attacks.<br><br>

			<h3>Warnings</h3><br><br>
			You should not use diclofenac if you have a history of allergic reaction to aspirin or NSAIDs (non-steroidal anti-inflammatory 				drugs).<br><br>

			Diclofenac can increase your risk of fatal heart attack or stroke, especially if you use it long term or take high doses, or 				if you have heart disease. Do not use this medicine just before or after heart bypass surgery (coronary artery bypass graft, 				or CABG).<br><br>

			Diclofenac may also cause stomach or intestinal bleeding, which can be fatal. These conditions can occur without warning while 				you are using this medicine, especially in older adults.<br><br>

			<h3>Before taking this medicine</h3><br><br>
			Diclofenac can increase your risk of fatal heart attack or stroke, even if you don't have any risk factors. Do not use this 				medicine just before or after heart bypass surgery (coronary artery bypass graft, or CABG).<br><br>

			Diclofenac may also cause stomach or intestinal bleeding, which can be fatal. These conditions can occur without warning while 				you are using this medicine, especially in older adults.<br><br>

			You should not use diclofenac if you are allergic to it, or if you have ever had an asthma attack or severe allergic reaction 				after taking aspirin or an NSAID.<br><br>

			Do not use Cambia to treat a cluster headache. Do not use Zipsor if you are allergic to beef or beef protein.<br><br>

			To make sure this medicine is safe for you, tell your doctor if you have:<br><br>

			heart disease, high blood pressure;<br><br>
			ulcers or bleeding in your stomach;<br><br>
			asthma;<br><br>
			liver or kidney disease; or<br><br>
			if you smoke.<br><br>
			Diclofenac can affect ovulation and it may be harder to get pregnant while you are using this medicine.<br><br>

			If you are pregnant, you should not take diclofenac unless your doctor tells you to. Taking a NSAID during the last 20 weeks 				of pregnancy can cause serious heart or kidney problems in the unborn baby and possible complications with your 			pregnancy.<br><br>

			It may not be safe to breastfeed while using this medicine. Ask your doctor about any risk.<br><br>

			Diclofenac is not approved for use by anyone younger than 18 years old.<br><br>

			<h3>How should I take diclofenac?</h3><br><br>
			Take diclofenac exactly as prescribed by your doctor. Follow the directions on your prescription label and read all medication 				guides. Your doctor may occasionally change your dose. Use the lowest dose that is effective in treating your 				condition.<br><br>

			Different brands of diclofenac contain different amounts of diclofenac, and may have different uses. If you switch brands, 				your dose needs may change. Follow your doctor's instructions about how much medicine to take. Ask your pharmacist if you have 				any questions about the brand you receive at the pharmacy.<br><br>

			Swallow the tablet whole and do not crush, chew, or break it.<br><br>

			Take Zorvolex on an empty stomach, at least 1 hour before or 2 hours after a meal.<br><br>

			Dissolve the powder form of this medicine (Cambia) with 1 to 2 ounces of water. Do not use any other type of liquid. Stir this 				mixture and drink all of it right away. Cambia works best if you take it on an empty stomach.<br><br>

			Call your doctor if your headache does not completely go away after taking Cambia.<br><br>

			If you use diclofenac long-term, you may need frequent medical tests.<br><br>

			Store at room temperature away from moisture and heat. Keep the bottle tightly closed when not in use.<br><br>

			<h3>Dosing information</h3><br><br>
			<u>Usual Adult Dose for Osteoarthritis:</u><br><br>

			Diclofenac free acid capsules: 35 mg orally 3 times a day<br><br>
			Diclofenac potassium immediate-release tablets: 50 mg orally 2 or 3 times a day<br><br>
			Diclofenac sodium enteric-coated tablets: 50 mg orally 2 or 3 times a day or 75 mg orally 2 times a day<br><br>
			Maximum dose: 150 mg daily<br><br>

			Diclofenac sodium extended-release tablets: 100 mg orally once a day<br><br>

			Use: For the relief of signs and symptoms of osteoarthritis.<br><br>

			<u>Usual Adult Dose for Ankylosing Spondylitis:</u><br><br>

