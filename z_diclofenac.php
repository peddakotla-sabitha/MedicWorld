			Maximum dose: 125 mg per day<br><br>

			Use: For acute or long-term use in the relief of signs and symptoms of ankylosing spondylitis.<br><br>

			<u>Usual Adult Dose for Dysmenorrhea:</u><br><br>

			Diclofenac potassium immediate-release tablets: 50 mg orally 3 times a day<br><br>

