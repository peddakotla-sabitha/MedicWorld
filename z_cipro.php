				<h2>What is Cipro?</h2><br><br>
					Cipro is a fluoroquinolone (flor-o-KWIN-o-lone) antibiotic that fights bacteria in the body.<br><br>

					Cipro is used to treat different types of bacterial infections. Cipro is also used to treat people who have 						been exposed to anthrax or certain types of plague.<br><br>

					Fluoroquinolone antibiotics can cause serious or disabling side effects that may not be reversible.<br><br>

					Cipro should be used only for infections that cannot be treated with a safer antibiotic.<br><br>

					<h3>Warnings</h3><br><br>
					Cipro can cause serious side effects, including tendon problems, nerve damage, serious mood or behavior 					changes, or low blood sugar.<br><br>

					Stop using this medicine and call your doctor at once if you have: headache, hunger, irritability, numbness, 						tingling, burning pain, confusion, agitation, paranoia, problems with memory or concentration, thoughts of 						suicide, or sudden pain or movement problems in any of your joints.<br><br>

					In rare cases, ciprofloxacin may cause damage to your aorta, which could lead to dangerous bleeding or death. 						Get emergency medical help if you have severe and constant pain in your chest, stomach, or back.<br><br>

					You may not be able to use Cipro if you have a muscle disorder. Tell your doctor if you have a history of 						myasthenia gravis.<br><br>

					<h3>Before taking this medicine</h3><br><br>
					You should not use Cipro if you are allergic to ciprofloxacin, or if:<br><br>

					you also take tizanidine; or<br><br>

					you are allergic to other fluoroquinolones (levofloxacin, moxifloxacin, norfloxacin, ofloxacin).<br><br>

					Ciprofloxacin may cause swelling or tearing of a tendon (the fiber that connects bones to muscles in the 						body), especially in the Achilles' tendon of the heel. This can happen during treatment or several months 						after you stop taking Cipro. Tendon problems may be more likely in children and older adults, or people who 						use steroid medicine or have had an organ transplant.<br><br>

					To make sure Cipro is safe for you, tell your doctor if you have ever had:<br><br>

					arthritis or problems with your tendons, bones or joints (especially in children);<br><br>

					diabetes, low blood sugar;<br><br>

					nerve problems;<br><br>

					an aneurysm or blood circulation problems;<br><br>

					heart problems, or a heart attack;<br><br>

					muscle weakness, myasthenia gravis;<br><br>

					liver or kidney disease;<br><br>

					a seizure, head injury, or brain tumor;<br><br>

					trouble swallowing pills;<br><br>

					long QT syndrome (in you or a family member); or<br><br>

					low levels of potassium in your blood (hypokalemia).<br><br>

					Do not give Cipro to a child without medical advice.<br><br>

					It is not known whether this medicine will harm an unborn baby. Tell your doctor if you are pregnant.<br><br>

					You should not breastfeed while taking ciprofloxacin and for 2 days after your last dose. Ask your doctor 						about breastfeeding if you take ciprofloxacin for anthrax exposure.<br><br>

					<h3>How should I take Cipro?</h3><br><br>
					Take Cipro exactly as prescribed by your doctor. Follow all directions on your prescription label and read all 						medication guides or instruction sheets.<br><br>

					Take Cipro at the same time each day, with or without food.<br><br>

					Shake the Cipro oral suspension (liquid) for 15 seconds before you measure a dose. Use the dosing syringe 						provided, or use a medicine dose-measuring device (not a kitchen spoon). Do not give Cipro oral suspension 						through a feeding tube.<br><br>

					Drink plenty of liquids while you are taking this medicine .<br><br>

					Use this medicine for the full prescribed length of time, even if your symptoms quickly improve. Skipping 						doses can increase your risk of infection that is resistant to medication. Ciprofloxacin will not treat a 						viral infection such as the flu or a common cold.<br><br>

					Do not share Cipro with another person.<br><br>

					Store at room temperature away from moisture and heat. Do not allow the liquid medicine to freeze. Throw away 						any unused liquid after 14 days.<br><br>



	
