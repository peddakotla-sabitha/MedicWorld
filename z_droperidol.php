			<h2>What is droperidol?</h2><br><br>
				Droperidol is a sedative, tranquilizer, and anti-nausea medicine<br><br>.

				Droperidol is used to reduce nausea and vomiting caused by surgery or other medical procedures.<br><br>

				Droperidol may also be used for purposes not listed in this medication guide.<br><br>

				 <h3>Warnings</h3><br><br>
				Tell your caregivers at once if you have sudden dizziness with fast or pounding heartbeats, fluttering in your chest, 					or trouble breathing.<br><br>

				<h3>Before taking this medicine</h3><br><br>
				You should not be treated with droperidol if you are allergic to it, or if you have a personal or family history of 					long QT syndrome.<br><br>

				Tell your doctor if you have ever had:<br><br>

				congestive heart failure;<br><br>

				very slow heartbeats;<br><br>

				high blood pressure;<br><br>

				an electrolyte imbalance (such as low levels of potassium or magnesium in your blood);<br><br>

				liver or kidney disease;<br><br>

				pheochromocytoma (tumor of the adrenal gland); or<br><br>

				alcoholism.<br><br>

				<h3>How is droperidol given?</h3><br><br>
				Before you receive droperidol, your heart function may need to be checked using an electrocardiograph or ECG 					(sometimes called an EKG).<br><br>

				Droperidol is injected into a muscle, under the skin, or into a vein. You will receive this injection in a medical 					setting prior to and/or during your surgery or medical procedure.<br><br>

				Your breathing, blood pressure, oxygen levels, kidney function, and other vital signs will be watched closely.<br><br>


	
