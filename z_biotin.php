				<h2>What is biotin?</h2><br><br>
				Biotin is a form of vitamin B found in foods. Biotin helps the body break down fats, carbohydrates, and 				proteins.<br><br>

				Biotin has been used in alternative medicine as a likely effective aid in treating or preventing biotin deficiency. 					Biotin deficiency can be caused by malnutrition, rapid weight loss, long-term tube feeding, and other medical 					conditions.<br><br>

				Biotin has also been used to treat seborrhea (skin rash) in babies. However, research has shown that biotin may not be 					effective in treating this condition.<br><br>

				Other uses not proven with research have included treating brittle nails or thinning hair, diabetes, nerve pain, and 					other conditions.<br><br>

				It is not certain whether biotin is effective in treating any medical condition. Medicinal use of this product has not 					been approved by the FDA. Biotin should not be used in place of medication prescribed for you by your doctor.<br><br>

				Biotin is often sold as an herbal supplement. There are no regulated manufacturing standards in place for many herbal 					compounds and some marketed supplements have been found to be contaminated with toxic metals or other drugs. 					Herbal/	health supplements should be purchased from a reliable source to minimize the risk of contamination.<br><br>

				Biotin may also be used for purposes not listed in this product guide.<br><br>

				Biotin side effects<br><br>
				Get emergency medical help if you have signs of an allergic reaction: hives; difficult breathing; swelling of your 					face, lips, tongue, or throat.<br><br>
		
				Common side effects may include upset stomach or diarrhea.<br><br>
	
				This is not a complete list of side effects and others may occur. Call your doctor for medical advice about side 					effects. You may report side effects to FDA at 1-800-FDA-1088.<br><br>

				<h3>Warnings</h3><br><br>
				Follow all directions on the product label and package. Tell each of your healthcare providers about all your medical 					conditions, allergies, and all medicines you use.<br><br>

				<h3>Before taking this medicine</h3><br><br>
				You should not use this product if you are allergic to biotin.<br><br>

				Before using biotin, talk to your healthcare provider. Your dose needs may be different:<br><br>
		
				if you have kidney disease;<br><br>

				if you have had stomach surgery; or<br><br>

				if you smoke.<br><br>

				Ask a doctor before using biotin if you are pregnant or breastfeeding. Your dose needs may be different during 					pregnancy or while you are nursing.<br><br>

				<h3>How should I take biotin?</h3><br><br>
				When considering the use of herbal supplements, seek the advice of your doctor. You may also consider consulting a 					practitioner who is trained in the use of herbal/health supplements.<br><br>

				If you choose to use biotin, use it as directed on the package or as directed by your doctor, pharmacist, or other 					healthcare provider. Do not use more of this product than is recommended on the label.<br><br>

				Measure liquid medicine carefully. Use the dosing syringe provided, or use a medicine dose-measuring device (not a 					kitchen spoon).<br><br>

				Biotin can cause false results with certain medical tests. Tell any doctor who treats you that you are using 					biotin.<br><br>

				The recommended dietary allowance of biotin increases with age. Follow your healthcare provider's instructions. You 					may also consult the Office of Dietary Supplements of the National Institutes of Health, or the U.S. Department of 					Agriculture (USDA) Nutrient Database (formerly "Recommended Daily Allowances") listings for more information.<br><br>

				It may take 3 to 6 months before the condition of your hair or nails improves.<br><br>

				Call your doctor if the condition you are treating with biotin does not improve, or if it gets worse while using this 					product.<br><br>

				After you stop using biotin, your nails will likely return to their original condition within 6 to 9 months.<br><br>

				Store at room temperature away from moisture and heat.<br><br>



	
