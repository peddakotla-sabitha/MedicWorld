			<h2>What is amoxicillin?</h2><br>
			Amoxicillin is a penicillin antibiotic that fights bacteria. Amoxicillin is used to treat many different types of infection 				caused by bacteria, such as tonsillitis, bronchitis, pneumonia, and infections of the ear, nose, throat, skin, or urinary 				tract.<br><br>

			Amoxicillin is also sometimes used together with another antibiotic called clarithromycin (Biaxin) to treat stomach ulcers 				caused by Helicobacter pylori infection. This combination is sometimes used with a stomach acid reducer called lansoprazole 				(Prevacid).<br><br>

			<h2>Warnings</h2><br>
			Do not use this medication if you are allergic to amoxicillin or to any other penicillin antibiotic, such as ampicillin 			(Omnipen, Principen), dicloxacillin (Dycill, Dynapen), oxacillin (Bactocill), penicillin (Beepen-VK, Ledercillin VK, Pen-V, 				Pen-Vee K, Pfizerpen, V-Cillin K, Veetids), and others.<br><br>

			Before using amoxicillin, tell your doctor if you are allergic to cephalosporins such as Omnicef, Cefzil, Ceftin, Keflex, and 				others. Also tell your doctor if you have asthma, liver or kidney disease, a bleeding or blood clotting disorder, 				mononucleosis (also called "mono"), or any type of allergy.<br><br>

			Amoxicillin can make birth control pills less effective. Ask your doctor about using a non-hormone method of birth control 				(such as a condom, diaphragm, spermicide) to prevent pregnancy while taking this medicine. Take this medication for the full 				prescribed length of time. Your symptoms may improve before the infection is completely cleared. Amoxicillin will not treat a 				viral infection such as the common cold or flu. Do not share this medication with another person, even if they have the same 				symptoms you have.<br><br>

			Antibiotic medicines can cause diarrhea. This may happen while you are taking amoxicillin, or within a few months after you 				stop taking it. This may be a sign of a new infection. If you have diarrhea that is watery or bloody, stop taking this 				medicine and call your doctor. Do not use anti-diarrhea medicine unless your doctor tells you to.<br><br>

			<h3>Before taking this medicine</h3><br><br>
			You should not use amoxicillin if you are allergic to any penicillin antibiotic, such as ampicillin, dicloxacillin, oxacillin, 				penicillin, or ticarcillin.<br>

			To make sure this medicine is safe for you, tell your doctor if you have:<br>

			kidney disease;<br>

			mononucleosis (also called "mono");<br>

			diarrhea caused by taking antibiotics; or<br>

			food or drug allergies (especially to a cephalosporin antibiotic such as Omnicef, Cefzil, Ceftin, Keflex, and others).<br>

			It is not known whether this medicine will harm an unborn baby. Tell your doctor if you are pregnant or plan to become 				pregnant.<br>

			Amoxicillin can make birth control pills less effective. Ask your doctor about using a non-hormonal birth control (condom, 				diaphragm, cervical cap, or contraceptive sponge) to prevent pregnancy.<br><br>
			
			 <h3>Dosage Forms and Strengths</h3><br>
			Amoxicillin Capsules USP 500 mg: Size “0” hard gelatin capsules, buff opaque cap and buff opaque body imprinted “CE” in black 				ink on cap and imprinted “114” in black ink on body, filled with white to off white crystalline powder, and contain 500 mg 				amoxicillin as the trihydrate.<br><br>

			<h3>How should I take amoxicillin?</h3><br>
			Take amoxicillin exactly as prescribed by your doctor. Follow all directions on your prescription label and read all 				medication guides or instruction sheets.

			Take amoxicillin at the same time each day.<br><br>

			Some forms of amoxicillin may be taken with or without food. Check your medicine label to see if you should take your mediicne 				with food or not.<br><br>

			Shake the oral suspension (liquid) before you measure a dose.<br><br>

			Measure liquid medicine with the dosing syringe provided, or use a medicine dose-measuring device (not a kitchen spoon). You 				may mix the liquid with water, milk, baby formula, fruit juice, or ginger ale. Drink all of the mixture right away. Do not 				save for later use.<br><br>

			You must chew the chewable tablet before you swallow it.<br><br>

			Swallow the regular tablet whole and do not crush, chew, or break it.<br><br>

			You will need frequent medical tests.<br><br>

			If you are taking amoxicillin with clarithromycin and/or lansoprazole to treat stomach ulcer, use all of your medications as 				directed. Read the medication guide or patient instructions provided with each medication. Do not change your doses or 				medication schedule without your doctor's advice.<br><br>

			Use this medicine for the full prescribed length of time, even if your symptoms quickly improve. Skipping doses can increase 				your risk of infection that is resistant to medication. Amoxicillin will not treat a viral infection such as the flu or a 				common cold.<br><br>

			Do not share this medicine with another person, even if they have the same symptoms you have.<br><br>

			This medicine can affect the results of certain medical tests. Tell any doctor who treats you that you are using this 				medicine .<br><br>

			Store at room temperature away from moisture, heat, and light.<br><br>

			You may store liquid amoxicillin in a refrigerator but do not allow it to freeze. Throw away any liquid mediicne that is not 				used within 14 days after it was mixed at the pharmacy.<br><br>






	
